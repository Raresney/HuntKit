"""Render a :class:`~huntkit.report.builder.Report` to Markdown, HTML, or JSON.

Each renderer walks the same model, so the three formats never disagree.
Markdown is the canonical, diff-friendly format; HTML is a self-contained,
print-ready page (inline CSS, no external assets); JSON is the machine form.
"""

from __future__ import annotations

import html as _html
import json
import time

from ..intel import Signal
from ..knowledge import Playbook
from .builder import Report

_GEN_BY = "Generated by HuntKit — for authorised testing only."


def _ts(epoch: float) -> str:
    return time.strftime("%Y-%m-%d %H:%M UTC", time.gmtime(epoch))


def _notable(report: Report) -> list[Signal]:
    """High/Critical single signals — the infra exposures worth calling out."""
    return sorted(
        (s for s in report.intel.signals if int(s.severity) >= 4),
        key=lambda s: (-int(s.severity), s.host),
    )


# ---------------------------------------------------------------------------
# JSON
# ---------------------------------------------------------------------------
def to_json(report: Report) -> str:
    return json.dumps(report.to_dict(), indent=2)


# ---------------------------------------------------------------------------
# Markdown
# ---------------------------------------------------------------------------
def _md_table(headers: list[str], rows: list[tuple[str, ...]]) -> list[str]:
    out = ["| " + " | ".join(headers) + " |",
           "| " + " | ".join("---" for _ in headers) + " |"]
    out += ["| " + " | ".join(row) + " |" for row in rows]
    out.append("")
    return out


def to_markdown(report: Report) -> str:
    intel = report.intel
    out: list[str] = [f"# HuntKit Report — {report.program}", "",
                      f"_Generated {_ts(report.generated)}_", ""]
    if report.scope_in:
        out.append(f"**In scope:** {', '.join(report.scope_in)}")
    if report.scope_out:
        out.append(f"**Out of scope:** {', '.join(report.scope_out)}")
    out.append("")

    out += ["## Summary", ""]
    out += _md_table(["Priority", "Hosts"],
                     [(label, str(n)) for label, n in intel.summary.items()])
    out += [f"{len(intel.signals)} signals across {len(intel.hosts)} hosts.", ""]

    if intel.hosts:
        out += ["## Prioritised hosts", ""]
        out += _md_table(
            ["Host", "Priority", "Score", "Signals", "Playbooks"],
            [(h.host, h.priority.label, str(h.score), str(len(h.signals)),
              ", ".join(h.playbooks[:4]) or "—") for h in intel.hosts],
        )

    paths = intel.attack_paths()
    if paths:
        out += ["## Attack paths", ""]
        rows = []
        for p in paths:
            where = ", ".join(p.hosts[:4]) + (" …" if len(p.hosts) > 4 else "")
            rows.append((p.name, f"`{p.playbook}`", p.severity.label, str(len(p.hosts)), where))
        out += _md_table(["Playbook", "id", "Severity", "Hosts", "Where"], rows)

    hot = _notable(report)
    if hot:
        out += ["## Notable exposures", ""]
        out += _md_table(
            ["Severity", "Host", "Finding", "Evidence"],
            [(s.severity.label, s.host, s.title, s.evidence) for s in hot],
        )

    out += ["## Recon surface", ""]
    out += _md_table(["Stage", "Count"],
                     [(label, str(n)) for label, n in report.recon.items()])

    if report.playbooks:
        out += ["## Playbooks", "",
                "The bug classes this surface points at, in priority order.", ""]
        for pb in report.playbooks:
            out.append(pb.to_markdown(level=3))
            out.append("")

    out += ["---", "", _GEN_BY]
    return "\n".join(out).rstrip() + "\n"


